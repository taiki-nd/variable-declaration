/**
 * const, let等の変数宣言
 */

var val1 = "var変数";
console.log(val1);

// var変数は上書き可能

val1 = "var変数の上書き";
console.log(val1);

// var変数の再宣言
var val1 = "var変数の再宣言";
console.log(val1);

//-----------------------------

let val2 = "let変数";
console.log(val2);

// letは上書き可能

val2 = "let変数を上書き";
console.log(val2);

// letは再宣言不可
//let val2 = "let変数の再宣言";
